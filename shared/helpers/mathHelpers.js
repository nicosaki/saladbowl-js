var helpers = module.exports;

/**
 * @param a
 * @param b
 * @returns {*}
 */
helpers.add = function (a, b) {
  return a + b;
};

/**
 * @param a
 * @param b
 * @returns {*}
 */
helpers.subtract = function (a, b) {
  return a - b;
};

/**
 * @param a
 * @param b
 * @returns {*}
 */
helpers.multiply = function (a, b) {
  return a * b;
};

/**
 * @param a
 * @param b
 * @returns {*}
 */
helpers.divide = function (a, b) {
  return a / b;
};